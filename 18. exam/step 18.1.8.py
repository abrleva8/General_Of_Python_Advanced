# –≤ –∫–æ—Ç–æ—Ä–æ–º –Ω–∞–ø–∏—Å–∞–Ω –∫–æ–¥ –Ω–∞ —è–∑—ã–∫–µ Python. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –≤—ã–≤–æ–¥—è—â—É—é –Ω–∞ —ç–∫—Ä–∞–Ω –∏–º–µ–Ω–∞ –≤—Å–µ—Ö —Ñ—É–Ω–∫—Ü–∏–π –¥–ª—è –∫–æ—Ç–æ—Ä—ã—Ö
# –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –ø–æ—è—Å–Ω—è—é—â–∏–π –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π. –ë—É–¥–µ–º —Å—á–∏—Ç–∞—Ç—å, —á—Ç–æ –ª—é–±–∞—è —Å—Ç—Ä–æ–∫–∞, –Ω–∞—á–∏–Ω–∞—é—â–∞—è—Å—è —Å–æ —Å–ª–æ–≤–∞ def –∏ –ø—Ä–æ–±–µ–ª–∞,
# —è–≤–ª—è–µ—Ç—Å—è –Ω–∞—á–∞–ª–æ–º –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–∏. –§—É–Ω–∫—Ü–∏—è —Å–æ–¥–µ—Ä–∂–∏—Ç –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π, –µ—Å–ª–∏ –ø–µ—Ä–≤—ã–π —Å–∏–º–≤–æ–ª –ø—Ä–µ–¥—ã–¥—É—â–µ–π —Å—Ç—Ä–æ–∫–∏ - #.

filename = input()
with open(filename, 'r', encoding='utf-8') as file_input:
    data = file_input.readlines()

functions = [line for index, line in enumerate(data) if line.startswith('def')
             if index == 0 or not data[index - 1].startswith('#')]

functions = map(lambda x: x.split()[1], functions)
functions = list(map(lambda x: x[:x.rfind('(')], functions))
print(*functions if functions else ("Best Programming Team",), sep='\n')
